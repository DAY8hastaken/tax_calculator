  'бһ”бҹ’бһҡбһ¶бһҖбҹӢбһ…бҹҶбһҺбһјбһӣбһ–бһёбһ•бһӣбһ·бһҸбһ•бһӣбһ”бҹ’бһҡбҹҒбһ„бһҖбһ¶бһҸ бһ¬бһ’бһ“бһ’бһ¶бһ“бһ’бһҳбҹ’бһҳбһҮбһ¶бһҸбһ·бһ”бҹ’бһҡбһҫбһўбһҸбҹ’бһҡбһ¶ 30%бҹ”',
  'QIP бһҖбҹ’бһ“бһ»бһ„бһҡбһҷбҹҲбһ–бҹҒбһӣбһӣбһҫбһҖбһӣбҹӮбһ„бһўбһ¶бһ…бһ”бҹ’бһҡбһҫбһўбһҸбҹ’бһҡбһ¶ 0% бһҸбһ¶бһҳбһӣбһҖбҹ’бһҒбһҒбһҺбҹ’бһҢбҹ”'
